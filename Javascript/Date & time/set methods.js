var date = new Date()

date.setDate() 	            // Set the day as a number (1-31)
date.setFullYear() 	        // Set the year (optionally month and day)
date.setHours() 	        // Set the hour (0-23)
date.setMilliseconds() 	    // Set the milliseconds (0-999)
date.setMinutes() 	        // Set the minutes (0-59)
date.setMonth() 	        // Set the month (0-11)
date.setSeconds() 	        // Set the seconds (0-59)
date.setTime()              // Set time in milliseconds UNIX time